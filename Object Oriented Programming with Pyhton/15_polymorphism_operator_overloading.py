#Operator overloading --> When the same operator is allowed to have different meaning according to the context.

print(1 + 2) #being used for adding two numbers
print("Akib " + "Hossain") #being used for string concatenation
print([1,2,3] + [4,5,6]) #being used for merging two list

#Here the "+" operator is making behaviour like polymorphism
